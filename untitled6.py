# Initialize the model and tokenizer with error handling
class GraniteAI:
    def __init__(self):
        self.model_name = "ibm-granite/granite-3.0-2b-instruct"
        self.model = None
        self.tokenizer = None
        self.load_model()

    def load_model(self):
        try:
            print("ğŸ”„ Loading IBM Granite model... This may take 2-3 minutes.")

            # Quantization config for memory efficiency
            quantization_config = BitsAndBytesConfig(
                load_in_4bit=True,
                bnb_4bit_compute_dtype=torch.float16,
                bnb_4bit_use_double_quant=True,
                bnb_4bit_quant_type="nf4"
            )

            # Load tokenizer
            self.tokenizer = AutoTokenizer.from_pretrained(
                self.model_name,
                trust_remote_code=True
            )

            # Load model with quantization
            self.model = AutoModelForCausalLM.from_pretrained(
                self.model_name,
                quantization_config=quantization_config,
                torch_dtype=torch.float16,
                trust_remote_code=True,
                device_map="auto"
            )

            # Set pad token
            if self.tokenizer.pad_token is None:
                self.tokenizer.pad_token = self.tokenizer.eos_token
                self.tokenizer.pad_token_id = self.tokenizer.eos_token_id

            print("âœ… Model loaded successfully!")
            return True

        except Exception as e:
            print(f"âŒ Error loading model: {str(e)}")
            print("ğŸ”„ Trying fallback model...")
            try:
                # Fallback to a working alternative
                self.model_name = "microsoft/DialoGPT-medium"
                self.tokenizer = AutoTokenizer.from_pretrained(self.model_name)
                self.model = AutoModelForCausalLM.from_pretrained(
                    self.model_name,
                    torch_dtype=torch.float16,
                    device_map="auto"
                )
                if self.tokenizer.pad_token is None:
                    self.tokenizer.pad_token = self.tokenizer.eos_token
                print("âœ… Fallback model loaded successfully!")
                return True
            except Exception as e2:
                print(f"âŒ Fallback failed: {str(e2)}")
                return False

    def generate_response(self, prompt, max_new_tokens=300, temperature=0.7, top_p=0.9):
        """Generate response with comprehensive error handling"""
        if self.model is None or self.tokenizer is None:
            return "âŒ Model not loaded. Please restart and try again."

        try:
            # Format prompt properly
            formatted_prompt = f"Human: {prompt}\n\nAssistant: "

            # Tokenize input
            inputs = self.tokenizer.encode(
                formatted_prompt,
                return_tensors="pt",
                truncation=True,
                max_length=512
            )

            # Move to same device as model
            if torch.cuda.is_available():
                inputs = inputs.cuda()

            # Generate response
            with torch.no_grad():
                outputs = self.model.generate(
                    inputs,
                    max_new_tokens=max_new_tokens,
                    temperature=temperature,
                    top_p=top_p,
                    do_sample=True,
                    pad_token_id=self.tokenizer.eos_token_id,
                    repetition_penalty=1.1,
                    no_repeat_ngram_size=3
                )

            # Decode response
            full_response = self.tokenizer.decode(outputs[0], skip_special_tokens=True)

            # Extract only the new generated text
            response = full_response[len(formatted_prompt):].strip()

            # Clean up response
            if not response:
                response = "I apologize, but I couldn't generate a proper response. Please try rephrasing your request."

            return response

        except Exception as e:
            return f"âŒ Generation error: {str(e)}. Please try again with a shorter prompt."

